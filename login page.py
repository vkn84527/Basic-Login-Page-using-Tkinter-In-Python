from tkinter import *
t=Tk()
t.geometry('450x400')
t.resizable(0,0)
def login():
    f2=Frame(bg="#091e42") #hexadecimal number
    f2.place(x=0,y=0,width=450,height=400)
    u0=Label(f2,text="Welcome to Login Page",font=("tims",12),bg="red")
    u0.place(x=120,y=40)
    u1=Label(f2,text="Enter Name",font=("tims",12))
    u1.place(x=100,y=80)
    e1=Entry(f2,font=("",12))
    e1.place(x=220,y=80,width=120)
    u2=Label(f2,text="Enter Password",font=("tims",12))
    u2.place(x=100,y=130)
    e2=Entry(f2,font=("",12),show="*")
    e2.place(x=220,y=130,width=120)
    b1=Button(f2,text="Login",font=("times",15))
    b1.place(x=220,y=180)
    b1=Button(f2,text="<---",font=("times",15),command=home)
    b1.place(x=0,y=0)
